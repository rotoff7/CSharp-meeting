void PrintPicture(int[,] array)
{
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            if (j < array.GetLength(1) - 1 && array[i, j] == 1)
                Console.Write($"{array[i, j]} ");
            else if (j < array.GetLength(1) - 1)
                Console.Write("  ");
            else if (array[i, j] == 1)
                Console.Write($"{array[i, j]}");
            else Console.Write(" ");
        }
        Console.WriteLine();
    }
}

void PicturePaint(int[,] arrPic, int lineIndex, int columnIndex)
{
    if (arrPic[lineIndex, columnIndex] == 0)
    {
        arrPic[lineIndex, columnIndex] = 1;
        PicturePaint(arrPic, lineIndex + 1, columnIndex);
        PicturePaint(arrPic, lineIndex, columnIndex + 1);
        PicturePaint(arrPic, lineIndex - 1, columnIndex);
        PicturePaint(arrPic, lineIndex, columnIndex - 1);

    }
}

